package com.example.job_platform.DTO;

public class DashBoardDTO {

    public long totalEmployeies;
    public long totalRecruiters;
    public long totalJobs;
    public long totalApplications;

    public long getTotalEmployeies() {
        return totalEmployeies;
    }

    public void setTotalEmployeies(long totalEmployeies) {
        this.totalEmployeies = totalEmployeies;
    }

    public long getTotalRecruiters() {
        return totalRecruiters;
    }

    public void setTotalRecruiters(long totalRecruiters) {
        this.totalRecruiters = totalRecruiters;
    }

    public long getTotalJobs() {
        return totalJobs;
    }

    public void setTotalJobs(long totalJobs) {
        this.totalJobs = totalJobs;
    }

    public long getTotalApplications() {
        return totalApplications;
    }

    public void setTotalApplications(long totalApplications) {
        this.totalApplications = totalApplications;
    }

}
